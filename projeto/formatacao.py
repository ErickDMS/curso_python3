variavel = 'abc'
print (f'{variavel}')
print (f'{variavel: >10}') #10 espacos para direita
print (f'{variavel: <10}') #10 espacos para esquerda
print (f'{variavel: ^10}') #10 no centro de 10 espacos
print (f'{1000.32452345: ,.2f}')
print (f'O hexadecimal de 1500 e {1500:08X}')
print (f'{variavel!r}')



